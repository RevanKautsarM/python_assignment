while True:
 siswa = input ('Masukan Nilai Siswa \t:')

 if int(siswa) >= 90:
    print('Siswa Dinyatakan Lulus')

 elif int(siswa) >= 80:
    print('Nilai A')

 elif int(siswa) >= 70:
    print('Nilai B')

 elif int(siswa) >= 60:
    print('Nilai C')

 elif int(siswa) >= 50:
    print('Siswa Dinyatakan Gagal')